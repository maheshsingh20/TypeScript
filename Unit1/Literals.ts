//Literals --> once we declare literals we need to select our choice from these options 
let color: "red" | "blue" | "green" | "black";  //string literals
color = "red"; //valid selction
//color = "white";//wrong selection --not valid
console.log(color);
//integer literals
let nums: 1 | 2 | 3 | 4;
//nums = 7;// wrong selection not valid because it is not in our options
nums = 3;
console.log(nums);